let currentLang = 'pt-BR';

function setLang(lang) {
  currentLang = lang;
  const strings = i18n[lang];
  document.documentElement.lang = lang === 'pt-BR' ? 'pt-BR' : 'en';

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (strings[key]) {
      el.innerHTML = strings[key];
    }
  });

  document.querySelectorAll('.lang-pt, .lang-en').forEach(el => {
    el.classList.toggle('active',
      (el.classList.contains('lang-pt') && lang === 'pt-BR') ||
      (el.classList.contains('lang-en') && lang === 'en')
    );
  });

  localStorage.setItem('lang', lang);
}

function toggleLang() {
  setLang(currentLang === 'pt-BR' ? 'en' : 'pt-BR');
}

// Navbar scroll effect
window.addEventListener('scroll', () => {
  const navbar = document.getElementById('navbar');
  navbar.classList.toggle('scrolled', window.scrollY > 50);
});

// Mobile menu
const mobileToggle = document.getElementById('mobileToggle');
const navLinks = document.getElementById('navLinks');

mobileToggle.addEventListener('click', () => {
  navLinks.classList.toggle('open');
});

document.querySelectorAll('.nav-links a').forEach(link => {
  link.addEventListener('click', () => {
    navLinks.classList.remove('open');
  });
});

// Lang toggle
document.getElementById('langToggle').addEventListener('click', toggleLang);

// Intersection Observer for animations
const observerOptions = {
  threshold: 0.1,
  rootMargin: '0px 0px -50px 0px'
};

const observer = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('visible');
      observer.unobserve(entry.target);
    }
  });
}, observerOptions);

document.querySelectorAll('.section:not(.hero)').forEach(section => {
  section.style.opacity = '0';
  section.style.transform = 'translateY(30px)';
  section.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
  observer.observe(section);
});

document.querySelectorAll('.project-card, .game-card, .contact-card, .stat-card').forEach(el => {
  el.style.opacity = '0';
  el.style.transform = 'translateY(20px)';
  el.style.transition = 'opacity 0.5s ease, transform 0.5s ease';
  observer.observe(el);
});

document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('lang');
  setLang(savedLang || 'pt-BR');

  // Re-trigger for any elements added after
  setTimeout(() => {
    document.querySelectorAll('.section:not(.hero).visible').forEach(el => {
      el.style.opacity = '1';
      el.style.transform = 'translateY(0)';
    });
  }, 100);
});
